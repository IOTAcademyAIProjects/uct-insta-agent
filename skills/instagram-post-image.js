const { execSync } = require('child_process');
const path = require('path');

module.exports = {
  name: 'instagram_post_image',
  description: 'Post an image to Instagram with an AI-generated caption. Use this when the user wants to post an image to Instagram. Requires an image URL and optionally a description or tone.',
  
  parameters: {
    type: 'object',
    properties: {
      image_url: {
        type: 'string',
        description: 'The URL of the image to post to Instagram'
      },
      description: {
        type: 'string',
        description: 'Description of the image to generate caption from. If not provided, use a general description.'
      },
      tone: {
        type: 'string',
        description: 'Tone of caption: inspirational, casual, professional, funny. Default is casual.'
      }
    },
    required: ['image_url']
  },

  async run({ image_url, description = 'a beautiful image', tone = 'casual' }) {
    try {
      const scriptPath = path.join(__dirname, '..', 'pipelines', 'post-with-caption.js');
      const result = execSync(
        `node "${scriptPath}" "${image_url}" "${description}" "${tone}"`,
        { 
          cwd: path.join(__dirname, '..'),
          env: { ...process.env },
          timeout: 60000
        }
      ).toString().trim();
      
      console.log('Pipeline output:', result);
      
      if (result.includes('SUCCESS')) {
        const postId = result.match(/Post ID: (.+)/)?.[1] || 'unknown';
        return `✅ Posted to Instagram successfully!\nPost ID: ${postId}\nCaption was generated by Claude AI.\nCheck: https://instagram.com/iot_academy_projects`;
      } else {
        return `❌ Failed to post: ${result}`;
      }
    } catch (error) {
      return `❌ Error: ${error.message}`;
    }
  }
};
